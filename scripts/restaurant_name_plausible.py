def is_chat_junk_extracted_name(s: str) -> bool:
    """
    True אם הטקסט נראה כמו שארית משיחה / זמן / מטא — לא שם מסעדה.
    משמש חילוץ צ'אט ו־is_plausible_restaurant_name.
    """
    if not (s or "").strip():
        return True
    t = _strip_outer_noise(s)
    if _NON_VENUE_TIME_OR_DURATION.search(t):
        return True
    # שורת יצירת קשר בהפקות/אירועים («*לשאלות - שרון 054…*») — לא שם מסעדה
    if re.search(r"לשאלות\s*[-–:]", t):
        return True
    low = t.casefold()
    for sub in _CHAT_NON_VENUE_SUBSTRINGS:
        if sub.casefold() in low:
            return True
    return False


def is_plausible_restaurant_name(name: str) -> bool:
    """
    False for obvious non-venues (cities, \"איטלקית ברעננה\", \"בין X לY\", junk punctuation).
    Curated / structured rows should still pass if they look like real names.
    """
    raw = name or ""
    s = _strip_outer_noise(raw)
    if len(s) < 2:
        return False
    if is_chat_junk_extracted_name(s):
        return False
    curated_long = _curated_style_long_ok(s)
    max_len = 76 if curated_long else 50
    if len(s) > max_len:
        return False

    if _BAD_LEADING.match(s):
        return False
    if _REJECT_LEADING_BE.match(s):
        return False
    if re.match(r"^(?:על|עם|של|ו)\s", s):
        return False
    if _REJECT_SENTENCE_LIKE_START.match(s):
        return False
    if _REJECT_AZEL_INFORMAL.match(s) or _REJECT_AZEL_THE.match(s):
        return False
    if _CONVERSATIONAL_OR_SPAM_IN_NAME.search(s):
        return False
    if not curated_long and "?" in s:
        return False
    if s.startswith("(") and "בין" in s[:40]:
        return False
    if re.search(r"בין\s+.+\s+ל(?:תל|ירושלים|חיפה|השרון|מרכז|צפון|דרום)", s):
        return False

    low = s.casefold()
    for sub in _REJECT_SUBSTRING:
        if sub.casefold() in low:
            return False

    if "?" in s[:3] or s.startswith("?"):
        return False

    if _BAD_LEADING_PHRASE.match(s):
        return False

    # שאלות כלליות — לא שם מקום ("מסעדה *כשרה* ברעננה?")
    if re.match(r"^\*?כשרה\*?(?:\s*\(|\s+ב)", s):
        return False

    core = _core_name(s)
    if core in _GEO_OR_GENERIC_EXACT:
        # «שוק מחנה יהודה» גנרי; «שוק מחנה יהודה (מוריס ועוד)» מזוהה — לא לזרוק בגלל הליבה הגאוגרפית
        if "(" not in s:
            return False

    solo = _strip_outer_noise(s).casefold()
    if solo in {w.casefold() for w in _SOLO_WORD_REJECT}:
        return False

    if _CUISINE_THEN_PLACE.match(s):
        return False
    if _DESCRIPTOR_HEAD.match(s):
        return False
    if _REQUEST_LIKE_RESTAURANT_NAME.search(s):
        return False

    parts = s.split()
    max_words = 12 if curated_long else 7
    if len(parts) > max_words:
        return False
    if _emoji_heavy(s):
        return False

    if len(parts) >= 2 and parts[0] in _DESCRIPTOR_LEAD and parts[1].startswith("ב"):
        return False

    if not curated_long:
        if parts and parts[0] in _FOOD_WORD_START:
            if len(parts) >= 5:
                return False
            if len(parts) >= 3 and parts[1] in _FOOD_AFTER_CATEGORY:
                return False
        if parts and parts[0] in ("מסעדה", "מסעדת") and len(parts) >= 6:
            return False

    # שאריות מסעדת (...) בשאלה
    if re.match(r"^\([^)]{0,60}\)\s+בשוק", s):
        return False
    if re.match(r"^\([^)]*\)\s*$", s) and len(s) < 40:
        return False

    # שורות שמתחילות בסוגר סוגר משמאל בלי שם — לרוב שבר מ-regex
    if re.match(r"^\)\s+", s):
        return False

    # בעיקר תיאור כתובת / הוראות
    if re.search(r"אחרי הכיכר|ימינה|שמאלה|בכניסה לטירה(?!\s*—)", s) and len(s) > 25:
        return False

    # משפטים שמתארים אירוע / כמות אנשים / שעות
    if re.search(
        r"(?:ל\d+\s+איש|יום שישי בערב|בשישי בערב|בשבת|לאירוע|למשפחה גדולה|לצהריי|לבר מצווה|לחגוג|שפתוחה היום|שפתוח עכשיו|24ש|050-|052-|054-)",
        s,
    ):
        return False

    return True
