def _curated_style_long_ok(s: str) -> bool:
    """שמות ארוכים שמקורם ברשימה הידנית — לא לחתוך."""
    return " — " in s or " / " in s


def _emoji_heavy(s: str) -> bool:
    n = 0
    for c in s:
        o = ord(c)
        if o > 0xFFFF or 0x1F300 <= o <= 0x1FAFF:
            n += 1
    return n > 4

