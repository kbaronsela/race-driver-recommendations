"""
Infer short Hebrew labels for extra_info (JSON field) from name + note + field.
Used by whatsapp_to_recommendations and batch updates.
"""
import re
from typing import List, Tuple


def normalize_infer_text(s):
    if not s:
        return ""
    s = s.replace("\u00a0", " ").replace("\u200f", "").replace("\u200e", "")
    s = re.sub(r"\s+", " ", s).strip()
    return s


def _text(name, note, note_max=600):
    n = normalize_infer_text(name or "")
    nt = normalize_infer_text((note or "")[:note_max])
    return (n + " " + nt).strip()


def _first_match(text: str, pairs: List[Tuple[str, str]]) -> str:
    """First (needle, label) where needle in text; needles should be ordered most specific first."""
    for needle, label in pairs:
        if needle and needle in text:
            return label
    return ""


