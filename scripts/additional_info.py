def infer_additional_info(name: str, note: str, field: str) -> str:
    """Return a short label (1–3 words) or empty string."""
    f = (field or "").strip()
    nm = normalize_infer_text(name or "")
    full = _text(name, note)
    if not full and not nm:
        return ""

    # מספרה — ילדים
    if f == "מספרה":
        kid_markers = ("ילדים", "לילדים", "לקטנים", "קטנים", "תינוק", "פעוט", "בנים", "בנות")
        if any(m in nm for m in kid_markers):
            return "ילדים"
        if any(m in full for m in ("מספרה לילדים", "ספר ילדים", "ספרית ילדים", "ספר לילדים")):
            return "ילדים"
        return ""

    if f == "רפואה":
        hit = _first_match(full, _MEDICINE)
        if hit:
            return hit
        if "ילדים" in full or "ילד" in nm:
            return "ילדים"
        return ""

    if f == "רפואת שיניים":
        hit = _first_match(full, _DENTAL)
        if hit:
            return hit
        return ""

    if f == "רפואה משלימה":
        hit = _first_match(full, _COMPLEMENTARY)
        if hit:
            return hit
        return ""

    if f == "פארא-רפואה":
        hit = _first_match(full, _PARA)
        if hit:
            return hit
        return ""

    if f == "מורים פרטיים":
        if "רואה חשבון" in full or "רואת חשבון" in full:
            return ""
        # קודם בשם — מונע שגיאה כשבהערה מופיעים כמה מקצועות (למשל "לשון ואנגלית")
        hit = _first_match(nm, _MORIM)
        if hit:
            return hit
        hit = _first_match(full, _MORIM)
        if hit:
            return hit
        return ""

    if f == "נגינה":
        hit = _first_match(full, _NEGINA)
        if hit:
            return hit
        return ""

    if f == "נהיגה":
        if "אוטומט" in full or "אוטומטי" in full:
            return "אוטומט"
        if "ידני" in full or "מני" in full:
            return "ידני"
        return ""

    if f == "עיסוי":
        if "רפלקס" in full:
            return "רפלקסולוגיה"
        if "שיאצו" in full:
            return "שיאצו"
        return ""

    if f == "פסיכולוגיה":
        comb_cf = ((name or "") + " " + (note or "")).casefold()
        if "cbt" in comb_cf:
            return "CBT"
        if "ילד" in full or "נוער" in full:
            return "ילדים ונוער"
        return ""

    if f == "אילוף כלבים":
        if "פנסיון" in full or "פנסיון" in nm:
            return "פנסיון"
        return ""

    if f == "קוסמטיקה":
        if "מניקור" in full:
            return "מניקור"
        if "פדיקור" in full:
            return "פדיקור"
        return ""

    if f == "חוגים":
        if "רקמה" in full:
            return "רקמה"
        return ""

    if f == "עריכת דין":
        if "מקרקעין" in full or "נדל" in full:
            return "מקרקעין"
        if "משפחה" in full:
            return "דיני משפחה"
        return ""

    if f == "גינון וגיזום":
        if "גיזום" in full and "עצים" in full:
            return "גיזום עצים"
        if "אגרונומ" in full:
            return "אגרונומיה"
        return ""

    if f == "תיירות":
        if "צימר" in full:
            return "צימרים"
        return ""

    if f == "מוסך":
        if "צמיג" in full:
            return "צמיגים"
        return ""

    if f == "דפוס":
        if "חולצות" in full or "הדפסה" in full:
            return "הדפסה"
        return ""

    if f == "אופטיקה":
        if "ילדים" in full:
            return "ילדים"
        return ""

    if f == "נגרות":
        if "דלת" in full:
            return "דלתות"
        if "פרגול" in full:
            return "פרגולות"
        if "מטבח" in full or "ארון" in full:
            return "מטבחים וארונות"
        return ""

    if f == "אינסטלציה":
        if "ביוב" in full or "ביובית" in full:
            return "ביוב"
        if "דוד שמש" in full or "דודי שמש" in full:
            return "דוד שמש"
        return ""

    if f == "חשמל":
        if "תאורה" in full or "תאור" in full:
            return "תאורה"
        return ""

    if f == "צילום":
        if "חתונה" in full or "חתונות" in full:
            return "אירועים"
        if "ילדים" in full or "משפחה" in full:
            return "משפחה וילדים"
        return ""

    if f == "הובלות":
        if "פסנתר" in full:
            return "פסנתר"
        return ""

    return ""
