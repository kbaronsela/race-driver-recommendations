"""Heuristics: drop chat-extracted \"names\" that are locations, adjectives, or phrases — not venue names.

Strict mode: short max length (50 chars unless name has \" / \" or \" — \"), leading ב+מיקום (כולל «בפארק …»),
descriptor-led phrases, generic food-category sentences, event/phone patterns, many substrings.
"""

from __future__ import annotations

import re
import unicodedata


def _nfc(s: str) -> str:
    return unicodedata.normalize("NFC", (s or "").strip())


def _strip_outer_noise(s: str) -> str:
    s = _nfc(s)
    s = s.strip(' "\'"״׳-–—')
    prev = None
    while prev != s:
        prev = s
        s = re.sub(r"^\*+\s*|\s*\*+$", "", s).strip()
    s = re.sub(r"\s+", " ", s).strip()
    return s


def _core_name(name: str) -> str:
    """Lowercase key without trailing (…) for geo / stop checks."""
    s = _strip_outer_noise(name)
    s = re.sub(r"\s*\([^)]{0,80}\)\s*$", "", s).strip()
    return s.casefold()


